package Day_2_Notes;

public class Type_Casting {

	
	
	public static void main(String[] args) {
		
		/*
		 * byte takes 1 byte
		 * double takes - 1 bytes
		 * short takes - 2 bytes
		 * float takes - 2 bytes
		 * int takes - 4 bytes
		 * Long takes -8 bytes
		 * 
		 * 
		 * Implicit TypeCasting-- convert a data lower to higher byte it happens automatically
		 *
		 * Explicit TypeCasting-- converting a data from higher to lower it will happen by user
		 *       data_type a = values;
		 *       Q a = (Q) a; explicit cating
		 */
		
		double d = 15.12;
		int a = (int) d;
		System.out.println(d);
		
		 
		
		
	}
	
}
